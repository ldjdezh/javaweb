package cn.ldj.domain;

public class ShopCar {
	private MobileForm mf;
	private int num;
	private double price;

	public MobileForm getMf() {
		return mf;
	}

	public void setMf(MobileForm mf) {
		this.mf = mf;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

}
